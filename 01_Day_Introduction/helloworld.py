# Checking data types

print(type(3)) # int
print(type(3.14)) # float
print(type(1 + 8j)) # complex
print(type("Hello World!")) # str
print(type([1, 2, 3])) # list
print(type({1: "one", 2: "two", 3: "three"})) # dict
print(type({1, 2, 3})) # set
print(type((1, 2, 3))) # tuple

